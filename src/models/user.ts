import mongoose from 'mongoose';

const { Schema } = mongoose;

const schema = new Schema({
  firstName: {
    type: String,
  },
  lastName: {
    type: String,
  },
  email: {
    type: String,
  },
  password: {
    type: String,
  },
  age: {
    type: Number,
  },
  gender: {
    type: String,
  },
});

export default mongoose.model('User', schema);
